$(document).ready(function(){
    $('#register-button').click(function(){
        $('.right').animate({
            'left':'0',
        },20).addClass('border-radius');
        $('.left').animate({
            'left':'50%',
        },300)
        $(this).parent().hide();
        $('.hide').show();
        $('.login-form').hide('fast');
        $('.register-form').show('fast')
    });
    $('#login-button').click(function(){
        $('.right').animate({
            'left':'50%',
        },300).removeClass('border-radius');
        $('.left').animate({
            'left':'0%',
        },300)
        $(this).parent().hide();
        $('.show').show();
        $('.login-form').show('fast');
        $('.register-form').hide('fast')
    });
    $('#register-button1').click(function(){
        $('.right').animate({
            'top':'50%',
        },20).addClass('border-radius');
        $('.left').animate({
            'bottom':'100%',
        },300)
        $(this).parent().hide();
        $('.hide').show();
        $('.login-form').hide('fast');
        $('.register-form').show('fast')
    });
    $('#login-button1').click(function(){
        $('.right').animate({
            'top':'0',
        },300).removeClass('border-radius');
        $('.left').animate({
            'bottom':'50%',
        },300)
        $(this).parent().hide();
        $('.show').show();
        $('.login-form').show('fast');
        $('.register-form').hide('fast')
    })
})